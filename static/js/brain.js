function convertToNumberArray(word1, word2){
    const alphabet = "qwertyuiopasdfghjklzxcvbnm";

    word1 = word1.toLowerCase();
    word2 = word2.toLowerCase();

    word1 = word1.replace(/\s/g, '');
    word2 = word2.replace(/\s/g, '');

    if(word1.length > 12){
        word1 = word1.substring(0, 12);
    }
    else if(word1.length < 12){
        const difference = 12 - word1.length;
        for(i = 0; i < difference; i++){
            word1 += alphabet[Math.floor(Math.random()*alphabet.length)];
        }
    }

    if(word2.length > 12){
        word2 = word2.substring(0, 12);
    }
    else if(word2.length < 12){
        const difference = 12 - word2.length;
        for(i = 0; i < difference; i++){
            word2 += alphabet[Math.floor(Math.random()*alphabet.length)];
        }
    }
    
    var array = [];

    for(i = 0; i < word1.length; i++){
        for(j = 0; j < alphabet.length; j++){
            if(alphabet[j] == word1[i]){
                array.push(j);
            }
        }
    }
    for(i = 0; i < word2.length; i++){
        for(j = 0; j < alphabet.length; j++){
            if(alphabet[j] == word2[i]){
                array.push(j);
            }
        }
    }

    return array;
}

function dataGenerator(word, label){
    const alphabet = "qwertyuiopasdfghjklzxcvbnm";
    var word2 = "";
    const numberOfChanges = Math.floor(word.length / 7);
    var points = [];

    word = word.toLowerCase();
    word = word.replace(" ", "")

    for(i = 0; i < numberOfChanges; i++){
        points.push(Math.floor(Math.random()*word.length));
    }
    if(label == 0){
        for(i = 0; i < word.length; i++){
            word2 += alphabet[Math.floor(Math.random()*alphabet.length)];
        }
    }
    else{
        for(i = 0; i < word.length; i++){
            var done = false;
            for(j = 0; j < points.length; j++){
                if(points[j] == i && done == false){
                    word2 += alphabet[Math.floor(Math.random()*alphabet.length)];
                    done = true;
                }
            }
            if(done == false){
                word2 += word[i];
            }
        }
    }

    return {input:convertToNumberArray(word, word2), output:[label]};
}

